<P align="left"><font color="#CC3300">
</font><font color="#CC3300" size="4">Notion 
d'ergodicité, théorie ergodique, mécanique statistique et mouvement brownien :</font></P></TD>
  </TR>
</TABLE>
		<p align="justify"><font size="4">Naïvement, l'<font color="#CC3300"><i>ergodicité</i></font>
		peut s'énoncer comme la propriété, pour un système mécanique donné, de
		converger en probabilité vers un état final indépendant de son état
		initial. En 1868, le physicien autrichien Ludwig Boltzmann (1844-1906)
		étudie et décrit le mouvement global des molécules dans les milieux gazeux
		(théorie cinétique des gaz) et crée quelques années plus tard (1877) la <font color="#CC3300"><i>
		mécanique statistique</i></font>, dont <a href="Maxwell.html">Maxwell</a>,
		par une approche distincte, est également à la base. Aux États-Unis, ce 
		point de vue sera développé par <a href="Gibbs.html">Gibbs</a>. Par <i>
		statistique</i>, on veut signifier une étude rendant compte du 
		comportement d'un grand nombre d'atomes ou molécules de même nature.</font></p>
		<P align="center">
<span style="background-color: #008080"><FONT SIZE="4" color="#FFFFFF"><b>&nbsp;i&nbsp;</b></FONT></span>&nbsp;<a title="Ctrl + Clic = Nouvel onglet." href="http://fr.wikipedia.org/wiki/Ludwig_Boltzmann">Ludwig Boltzmann</a> <font color="#008000">sur W<span style="font-variant: small-caps">ikipédi</span>A&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;
<b><font size="4" color="#F26100">&raquo;</font></b>&nbsp;
<a href="../anx/univers_quantique.html#boltz">Pauli</a></font></P>


<P align="justify"><font size="4">A l'origine de cette mécanique est la découverte
(1827) du

<font color="#CC3300"><i>mouvement  brownien</i></font>
(appellation est due à <font color="#800080">
<a href="levy.html">Paul
Lévy</a></font>) par le botaniste écossais Robert Brown (1773-1858)
décrivant des trajectoires apparemment désordonnées des particules de pollen
dans l'eau et s'interprétant aujourd'hui comme un <font color="#800080">
<a href="Markov.html">
processus markovien</a></font>
(sans <i>mémoire</i>). Il fut étudié par <a href="Wiener.html">Norbert Wiener</a> qui le 
modélisa au moyen de fonctions continues dérivables en aucun point et, plus 
récemment, par <a href="Kahane.html">J.-P. Kahane</a> </font>(<a href="#psp">réf. 
6 &amp; 7</a>)<font size="4"> :</font></P>
<script></script>
<P align="justify"><font size="4">La <font color="#800080">
<a href="Democrite.html">théorie atomiste</a></font>
viendra à
s'imposer et impliquer l'impossibilité de décrire un système physique à l'échelle
microscopique par un <font color="#800080">
<a href="Lagrange.html">système lagrangien</a>
</font>
d'équations différentielles en fonction du temps car il y aurait autant
d'équations que de particules ! Le physicien est amené à considérer des moyennes
d'observations s'interprétant comme des <font color="#800080">
<a href="../anx/loi_cont.html">
espérances mathématiques</a></font>
au sens mathématique du <i>calcul des probabilités</i>.</font></P>
<P align="justify"><font size="4">L'équation de Boltzmann, décrivant l'évolution
d'un gaz raréfié, très complexe car elle contient tout à la fois des
éléments d'intégration et de dérivation (<a href="BernoulliDan.html">équation aux dérivées partielles</a>), est un objet d'études mathématique actuel comme le montrent les deux médaillés
Fields <a href="lions_pl.html">Pierre-Louis Lions</a> (1994) et
<a href="Villani.html">Cédric Villani</a> (2010).</font></P>
<P align="center"><font color="#CC3300">Les médaillés Fields depuis 1936 :</font><a title="Lien vers externe.
Clic sur roulette = Nouvel onglet." href="../anx/med_fields.html"><b><font size="1" style="text-decoration: none">&nbsp;</font><font size="5" style="text-decoration: none" color="#0000FF">&raquo;</font></b></a>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
<b><font size="4" color="#FF8040">&raquo;</font></b>&nbsp;<a href="Fields.html">Fields</a></P>


<P align="justify"><font size="4">L'<font color="#CC3300"><i>hypothèse
ergodique</i></font> (du grec <i>ergon</i> =
<i>énergie</i>, qui a aussi fourni <font color="#800080"> <a href="Peaucellier.html">erg</a></font>) pour un
système isolé (conservant son énergie), énoncée par <a href="pauli.html#boltz">Boltzmann</a> en 1871 dans le cadre de sa théorie cinétique des
gaz, consiste à exprimer que ces moyennes
devraient converger avec le temps vers les mêmes limites que celles obtenues si
on avait pu calculer les moyennes de chaque particule du système.</font></P>
<P align="center"><font color="#CC3300">La notion d'entropie : </font><a title="Lien vers externe.
Clic sur roulette = Nouvel onglet." href="http://www.lerepairedessciences.fr/sciences/phystat/phystat_index.htm">
<font color="#CC3300">
</font>
<font color="#CC3300">
<a title="Lien vers externe.
Clic sur roulette = Nouvel onglet." href="../anx/univers_quantique.html#entropie">
<b><font size="5" style="text-decoration: none" color="#0000FF">&raquo;</font></b></a></font></a></P>
<P align="justify"><font size="4">En
mathématiques, <i><font color="#CC3300">la théorie ergodique</font></i>, à
laquelle s'attela <a href="Poincare.html">Poincaré</a> dans les années 1890, étudie le comportement limite des
<font color="#800080">
<a href="Yoccoz.html">systèmes dynamiques</a></font> en recherchant des propriétés invariantes. Ses outils sont
aujourd'hui la <font color="#800080"> <a href="Borel.html">théorie de la
mesure</a> </font>(mesure de <font color="#800080"> <a href="Lebesgue.html">
Lebesgue</a>,</font> qui manqua à <a href="Poincare.html">Poincaré</a> dans ses premières recherches), le calcul des probabilités, les <font color="#800080"> <a href="Markov.html">processus
stochastiques</a></font>, la <font color="#800080"> <a href="Riemann.html#diff">géométrie différentielle</a></font>.</font></P>
<P align="justify"><font size="4">On retrouve le principe ergodique en
<a href="../anx/Th_graphes.html#transi">théorie des graphes</a> (matrices de
transition) et en calcul des probabilités lorsqu'on énonce par exemple
<a href="Kolmogorov.html#lf">la loi forte des grands nombres</a>.</font></P>


