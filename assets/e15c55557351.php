<p align="justify"><font size="4">
Après des études secondaires au Prytanée
militaire de la flèche (Orne), Fortet entre à l'ENS. Agrégé de mathématiques
(1934), il soutiendra, sous la direction de <a href="Frechet.html">Maurice Fréchet</a> 
et <a href="Valiron.html">Georges Valiron</a>,
une thèse (1939) sur les <a href="Markov.html">processus markoviens</a> dans le
cas de chaînes dénombrables (à nombre infini d'états) définies dans des
espaces de <a href="Banach.html">Banach</a> plus précisément intitulée </font>
<font SIZE="4"><span LANG><i>Sur l'itération des substitutions algébriques 
linéaires à une infinité de variables et ses applications à la théorie des 
probabilités en chaîne</i></span> </font>(<font color="#008000"><font size="4" color="#F26100"><b>&raquo;</b></font></font>
<a href="#psp">réf.2a</a>)<font size="4">.</font></p>
<p align="justify"><font size="4">
Les travaux
de Fortet porteront alors sur la théorie des  processus aléatoires et
la <i><font color="#CC3300">théorie ergodique</font></i> dans le cadre de la
<a href="Borel.html">théorie de la mesure</a> et de l'intégrale de
<a href="Lebesgue.html">Lebesgue</a>, fondements mathématiques de la
théorie moderne des probabilités, axiomatisée en 1933 par le russe
<a href="Kolmogorov.html">
Andreï Kolmogorov</a>.</font></p>

<p align="justify"><font size="4">Fortet fut professeur à la
faculté des sciences de Caen et de Paris ainsi qu'à l'<a title="Lien vers site externe.&#013;Clic gauche pour rester dans
ChronoMath.&#013;Clic sur roulette pour nouvel onglet.&#013;Clic droit pour
nouvelle fenêtre." href="http://www.espci.fr/presentation/historique.htm">ESPCI</a>
(École supérieure de Physique et de Chimie Industrielles de la Ville de Paris), 
créée en 1882. </font></p>
<p align="justify"><font size="4">En 1960, succédant à <a href="Darmois.html">
Darmois</a> à la Sorbonne (chaire de calcul des probabilité et physique 
mathématique), il mit en place et dirigea le laboratoire de
probabilités de Paris-Jussieu (plus précisément, <i>Laboratoire de Probabilités 
et Modèles Aléatoires</i>, LPMA). Il fut 
également directeur de recherches au CNRS et élu
membre correspondant de l'Académie des sciences en 1973.</font></p>

